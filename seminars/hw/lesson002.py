# все возможные пары чисел X и Y через пробел, такие что X <= Y.
# Пример
# На входе:
# s = 12
# p = 27
# На выходе:
# 3 9

s = 12
p = 27
x=y=0
for i in range(1001):
    for j in range(1001):
        if (i+j)==s and (i*j)==p:
            if i<=j:
                x,y=i,j
            else:
                x,y=j,i
